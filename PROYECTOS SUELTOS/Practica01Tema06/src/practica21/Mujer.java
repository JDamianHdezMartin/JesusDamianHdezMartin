public class Mujer {
    String nombre;
    String apellido1;
    String apellido2;
    int edad;
    int altura;
    int peso;

    public Mujer() {
    }
    

    public Mujer(String nombre, String apellido1, String apellido2, int edad, int altura, int peso) {
        this.nombre = nombre;
        this.apellido1 = apellido1;
        this.apellido2 = apellido2;
        this.edad = edad;
        this.altura = altura;
        this.peso = peso;
    }
}
